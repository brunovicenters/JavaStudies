package SistemaDePonto;

public class SistemaDePonto {
    private double x;
    private double y;

    public SistemaDePonto(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
