package Livro;

public class Livro {
    private String titulo;
    private String autor;

    public Livro(java.lang.String titulo, java.lang.String autor) {
        this.titulo = titulo;
        this.autor = autor;
    }

    public java.lang.String getTitulo() {
        return titulo;
    }

    public java.lang.String getAutor() {
        return autor;
    }
}
